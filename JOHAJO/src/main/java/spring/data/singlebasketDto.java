package spring.data;

public class singlebasketDto {
	private int idx;
	private int fidx;
	private int midx;
	private int count;
	private String today;
	private int residx;
	private int nm_number;
	
	
	public int getNm_number() {
		return nm_number;
	}
	public void setNm_number(int nm_number) {
		this.nm_number = nm_number;
	}
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	public int getFidx() {
		return fidx;
	}
	public void setFidx(int fidx) {
		this.fidx = fidx;
	}
	public int getMidx() {
		return midx;
	}
	public void setMidx(int midx) {
		this.midx = midx;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public String getToday() {
		return today;
	}
	public void setToday(String today) {
		this.today = today;
	}
	public int getResidx() {
		return residx;
	}
	public void setResidx(int residx) {
		this.residx = residx;
	}
	
	
}
